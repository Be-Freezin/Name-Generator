function render(firstName, lastName){
    nameUl.textContent = ""
    for( let i = 0; i<10; i++){
        
        const nameLi = document.createElement('li')
        nameLi.className = "li-items"
        const randomFirstName = Math.floor(Math.random()* (firstName.length))
        const randomLastName = Math.floor(Math.random()* (lastName.length))
        
        nameLi.textContent += firstName[randomFirstName] + " " + lastName[randomLastName]
        nameUl.append(nameLi)
    }
}
